		Biblat es un portal especializado en revistas científicas y académicas publicadas en América Latina y el Caribe, que ofrece los siguientes servicios:
		<br></br>

		<ol>
			<li>Referencias bibliográficas de los artículos y documentos publicados en más de 3,000 títulos indizados en <a href="http://clase.unam.mx" target="_blank">CLASE</a> y <a href="http://periodica.unam.mx" target="_blank">PERIÓDICA</a>, mediante opciones de búsqueda básica y avanzada.</li>
			<li>Acceso al texto completo de los artículos publicados en revistas de acceso abierto. Biblat ofrece dos tipos de acceso al texto completo: mediante enlaces hipertextuales a los sitios web de las revistas (recursos externos) y a través de la colección del acervo digital de la Hemeroteca Virtual Latinoamericana de la Dirección General de Bibliotecas (<a href="http://dgb.unam.mx" target="_blank">DGB</a>), Universidad Nacional Autónoma de México (<a href="http://www.unam.mx" target="_blank">UNAM</a>).</li>
			<li>Frecuencias e <a href="http://biblat.unam.mx/es/indicadores" target="_blank">indicadores</a> bibliométricos extraídos de las revistas indizadas en <a href="http://clase.unam.mx" target="_blank">CLASE</a> y <a href="http://periodica.unam.mx" target="_blank">PERIÓDICA</a>, en las colecciones <a href="http://www.scielo.org" target="_blank">SciELO</a> así como de otras fuentes de información.</li>
			<li>En Postular una revista se describen los Criterios de selección de revistas para su indización en <a href="http://clase.unam.mx" target="_blank">CLASE</a> y <a href="http://periodica.unam.mx" target="_blank">PERIÓDICA</a>, que son la fuente de información principal de Biblat. Los editores de revistas pueden someter a evaluación su revista dirigiéndose a: <a href="mailto:biblat@dgb.unam.mx">biblat@dgb.unam.mx</a>.</li>
			<li>En Políticas de acceso se clasifican las revistas de acceso abierto de acuerdo con los permisos de acceso, uso y archivado adoptados por éstas.</li>
		</ol>

