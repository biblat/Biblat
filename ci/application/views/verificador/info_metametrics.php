<p>MetaMetrics valida los metadatos a nivel artículo de las revistas publicadas en Open Journal System (OJS). Esta aplicación se basa en el <a href="{base_url('archivos/pdf/ManualDeIndizacionEnOJS_BuenasPracticas.pdf')}" target="_blank">Manual de indización en OJS: Buenas prácticas para la región latinoamericana</a>, el cual constituye un marco de trabajo común para los países de América Latina y el Caribe.</p>
    <br><br>
La validación que realiza MetaMetrics comprende los siguientes aspectos:
</p>
<dd style="margin-left: 50px;">
    <b>Suficiencia</b>
    <br><br>
    La suficiencia significa que el registro está completo, tanto de la revista como de cada documento; también se considera la existencia de datos institucionales de los autores.
    <br><br>
    <b>Consistencia</b>
    <br><br>
    Asentamiento de los metadatos conforme a las normas ortográficas, sintaxis (estructura, tipo o formato de los datos) y semántica (campos con valores predefinidos) enunciadas en el <a href="{base_url('archivos/pdf/ManualDeIndizacionEnOJS_BuenasPracticas.pdf')}" target="_blank">Manual de indización en OJS: Buenas prácticas para la región latinoamericana</a>.
    <br><br>
    <b>Precisión</b>
    <br><br>
    Se considera una buena práctica que los datos de la revista coincidan con los registrados en el Portal ISSN, que se especifiquen los idiomas de publicación, además que los identificadores persistentes y enlaces a los textos completos se resuelvan correctamente. 
</dd>
